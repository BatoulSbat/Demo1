#while loop
sum1 = 0
i = 2 #initial even integer for the sum

while sum1 >= 250: 
     sum1 += i
     i += 2 #update statement, shorthand for i = i + 2
print(sum1)

#counting up to 10
i = 0

while i <= 10:
     print(i) # Print the current value of the i
     i += 1 # Increment i by 1 for the next iteration